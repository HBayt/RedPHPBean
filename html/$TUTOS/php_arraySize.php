
<?php
//_________________________________________________
// Syntax

// Using count() function
// $length = count($array);

// Using sizeof() function (alias of count())
// $length = sizeof($array);

//_________________________________________________

// SAMPLE 

// Define an array
$array = [1, 2, 3, 4, 5];

// Using count() function
$length = count($array);
echo "The length of the array is: $length";
echo "\n";

// Using sizeof() function
$length = sizeof($array);
echo "The length of the array is: $length";


// Output
// The length of the array is: 5
// The length of the array is: 5




?>
